package com.dhakre.rohit.demo;

public class TestOperator {

	public static void main(String[] args) {
		int a = 10;
		int b = 20;
		System.out.println(a++ + " " + b++);
		System.out.println(a++ + " " + ++b);
		System.out.println(++a + " " + b++);
		System.out.println(++a + " " + ++b);
		System.out.println();
		System.out.println();
		System.out.println();
		System.out.println();
	}

}
